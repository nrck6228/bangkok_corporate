					<p><strong>นโยบายการคุ้มครองข้อมูลส่วนบุคคล (Privacy Policy)</strong></p>
<p>—————————————————————————————————————</p>
<ol>
<li><strong>บทนำ</strong></li>
</ol>
<p>กรุงเทพมหานคร (ต่อไปในนโยบายนี้เรียกว่า <strong>“กทม.”</strong> ตระหนักถึงความสำคัญของข้อมูลส่วนบุคคลและข้อมูลอื่นอันเกี่ยวกับท่าน (รวมเรียกว่า “<strong>ข้อมูล</strong>”) เพื่อให้ท่านสามารถเชื่อมั่นได้ว่า กทม. มีความโปร่งใสและความรับผิดชอบในการเก็บรวบรวม ใช้หรือเปิดเผยข้อมูลของท่านตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 (“<strong>กฎหมายคุ้มครองข้อมูลส่วนบุคคล</strong>”) รวมถึงกฎหมายอื่นที่เกี่ยวข้อง นโยบายการคุ้มครองข้อมูลส่วนบุคคล (“<strong>นโยบาย</strong>”) นี้จึงได้ถูกจัดทำขึ้นเพื่อชี้แจงแก่ท่านถึงรายละเอียดเกี่ยวกับการเก็บรวบรวม ใช้หรือเปิดเผย (รวมเรียกว่า “<strong>ประมวลผล</strong>”) ข้อมูลส่วนบุคคลซึ่งดำเนินการโดย กทม. รวมถึงเจ้าหน้าที่และบุคคลที่เกี่ยวข้องผู้ดำเนินการแทนหรือในนามของ กทม. โดยมีเนื้อหาสาระดังต่อไปนี้</p>
<ol>
<li><strong>ขอบเขตการบังคับใช้นโยบาย</strong></li>
</ol>
<p>นโยบายนี้ใช้บังคับกับ<strong>ข้อมูลส่วนบุคคลของบุคคลซึ่งมีความสัมพันธ์กับ </strong>กทม. ในปัจจุบันและที่อาจมีในอนาคต ซึ่งถูกประมวลผลข้อมูลส่วนบุคคลโดย กทม. เจ้าหน้าที่ พนักงานตามสัญญา หน่วยธุรกิจหรือหน่วยงานรูปแบบอื่นที่ดำเนินการโดย กทม. และรวมถึงคู่สัญญาหรือบุคคลภายนอกที่ประมวลผลข้อมูลส่วนบุคคลแทนหรือในนามของ กทม. (“<strong>ผู้ประมวลผลข้อมูลส่วนบุคคล</strong>”) ภายใต้การร่วมกิจกรรมและการดำเนินงานต่าง ๆ เช่น เว็บไซต์ ระบบ แอปพลิเคชัน เอกสาร หรือบริการในรูปแบบอื่นที่ควบคุมดูแลโดย กทม. (รวมเรียกว่า “<strong>บริการ</strong>”)</p>
<p>บุคคลมีความสัมพันธ์กับ กทม. ตามความในวรรคแรก รวมถึง</p>
<ol>
<li>บุคคลธรรมดา</li>
<li>เจ้าหน้าที่หรือผู้ปฏิบัติงาน ลูกจ้าง</li>
<li>กรรมการ ผู้รับมอบอำนาจ ผู้แทน ตัวแทน ลูกจ้าง หรือบุคคลอื่นที่มีความสัมพันธ์ในรูปแบบเดียวกันของนิติบุคคลที่มีความสัมพันธ์กับ กทม.</li>
<li>ผู้เข้าร่วมกิจกรรมของ กทม.</li>
<li>ผู้เข้าชมหรือใช้งานเว็บไซต์ www.50ปีกทม.com รวมทั้งระบบ แอปพลิเคชัน อุปกรณ์ หรือช่องทางการสื่อสารอื่นซึ่งควบคุมดูแลโดย กทม.</li>
</ol>
<p>ข้อ 1) ถึง 5) เรียกรวมกันว่า “<strong>ท่าน</strong>”</p>
<p>นอกจากนโยบายฉบับนี้แล้ว กทม. อาจกำหนดให้มี<strong>คำประกาศเกี่ยวกับความเป็นส่วนตัว</strong> (“<strong>ประกาศ</strong>”) สำหรับผลิตภัณฑ์หรือบริการของ กทม. เพื่อชี้แจงให้เจ้าของข้อมูลส่วนบุคคลซึ่งเป็นผู้ใช้บริการได้ทราบถึงข้อมูลส่วนบุคคลที่ถูกประมวลผล วัตถุประสงค์และเหตุผลอันชอบด้วยกฎหมายในการประมวลผล ระยะเวลาในการเก็บรักษาข้อมูลส่วนบุคคล รวมถึงสิทธิในข้อมูลส่วนบุคคลที่เจ้าของข้อมูลส่วนบุคคลพึงมีในบริการหรือกิจกรรมนั้นเป็นการเฉพาะเจาะจง</p>
<p>ทั้งนี้ ในกรณีที่มีความขัดแย้งกันในสาระสำคัญระหว่างความในประกาศเกี่ยวกับความเป็นส่วนตัวและนโยบายนี้ ให้ถือตามความในประกาศเกี่ยวกับความเป็นส่วนตัวของบริการนั้น</p>
<ol>
<li><strong>คำนิยาม</strong></li>
</ol>
<ul>
<li>กทม. หมายถึง กรุงเทพมหานคร</li>
<li><strong>ข้อมูลส่วนบุคคล </strong>หมายถึง ข้อมูลเกี่ยวกับบุคคลธรรมดา ซึ่งทำให้สามารถระบุตัวบุคคลนั้นได้ไม่ว่าทางตรงหรือทางอ้อม แต่ไม่รวมถึงข้อมูลของผู้ถึงแก่กรรมโดยเฉพาะ</li>
<li><strong>ข้อมูลส่วนบุคคลอ่อนไหว </strong>หมายถึง ข้อมูลส่วนบุคคลตามที่ถูกบัญญัติไว้ในมาตรา 26 แห่งพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 ซึ่งได้แก่ ข้อมูลเชื้อชาติ เผ่าพันธุ์ ความคิดเห็นทางการเมือง ความเชื่อในลัทธิ ศาสนาหรือปรัชญา พฤติกรรมทางเพศ ประวัติอาชญากรรม ข้อมูลสุขภาพ ความพิการ ข้อมูลสหภาพแรงงาน ข้อมูลพันธุกรรม ข้อมูลชีวภาพ หรือข้อมูลอื่นใดซึ่งกระทบต่อเจ้าของข้อมูลส่วนบุคคลในทำนองเดียวกันตามที่คณะกรรมการคุ้มครองข้อมูลส่วนบุคคลประกาศกำหนด</li>
<li><strong>การประมวลผลข้อมูลส่วนบุคคล </strong>หมายถึง การดำเนินการใด ๆ กับข้อมูลส่วนบุคคล เช่น เก็บรวบรวม บันทึก สำเนา จัดระเบียบ เก็บรักษา ปรับปรุง เปลี่ยนแปลง ใช้ กู้คืน เปิดเผย ส่งต่อ เผยแพร่ โอน รวม ลบ ทำลาย เป็นต้น</li>
<li><strong>เจ้าของข้อมูลส่วนบุคคล </strong>หมายถึง บุคคลธรรมดาซึ่งเป็นเจ้าของข้อมูลส่วนบุคคลที่ กทม. เก็บรวบรวม ใช้ หรือเปิดเผย</li>
<li><strong>ผู้ควบคุมข้อมูลส่วนบุคคล </strong>หมายถึง บุคคลหรือนิติบุคคลซึ่งมีอำนาจหน้าที่ตัดสินใจเกี่ยวกับการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคล</li>
<li><strong>ผู้ประมวลผลข้อมูลส่วนบุคคล </strong>หมายถึง บุคคลหรือนิติบุคคลซึ่งดำเนินการเกี่ยวกับการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลตามคำสั่งหรือในนามของผู้ควบคุมข้อมูลส่วนบุคคล ทั้งนี้ บุคคลหรือนิติบุคคลซึ่งดำเนินการดังกล่าวไม่เป็นผู้ควบคุมข้อมูลส่วนบุคคล</li>
</ul>
<ol>
<li><strong>แหล่งที่มาของข้อมูลส่วนบุคคลที่ กทม. เก็บรวบรวม</strong></li>
</ol>
<p>กทม. เก็บรวบรวมหรือได้มาซึ่งข้อมูลส่วนบุคคลประเภทต่าง ๆ จากแหล่งข้อมูลดังต่อไปนี้</p>
<ol>
<li>ข้อมูลส่วนบุคคลที่ กทม. เก็บรวบรวมจากเจ้าของข้อมูลส่วนบุคคลโดยตรงในช่องทางให้บริการต่าง ๆ เช่น ขั้นตอนการสมัคร เอกสาร ภาพถ่าย แบบสำรวจ บริการ หรือช่องทางให้บริการอื่นที่ควบคุมดูแลโดย กทม. หรือเมื่อเจ้าของข้อมูลส่วนบุคคลติดต่อสื่อสารกับ กทม. ณ ที่ทำการหรือผ่านช่องทางติดต่ออื่นที่ควบคุมดูแลโดย กทม. เป็นต้น</li>
<li>ข้อมูลที่ กทม. เก็บรวบรวมจากการที่เจ้าของข้อมูลส่วนบุคคลเข้าใช้งานเว็บไซต์ หรือบริการอื่น ๆ ตามสัญญาหรือตามพันธกิจ เช่น การติดตามพฤติกรรมการใช้งานเว็บไซต์ หรือบริการของ กทม. ด้วยการใช้คุกกี้ (Cookies) หรือจากซอฟต์แวร์บนอุปกรณ์ของเจ้าของข้อมูลส่วนบุคคล เป็นต้น</li>
<li>ข้อมูลส่วนบุคคลที่ กทม. เก็บรวบรวมจากแหล่งอื่นนอกจากเจ้าของข้อมูลส่วนบุคคล โดยที่แหล่งข้อมูลดังกล่าวมีอำนาจหน้าที่ มีเหตุผลที่ชอบด้วยกฎหมายหรือได้รับความยินยอมจากเจ้าของข้อมูลส่วนบุคคลแล้วในการเปิดเผยข้อมูลแก่ กทม. เช่น การเชื่อมโยงบริการดิจิทัลของหน่วยงานของรัฐในการให้บริการเพื่อประโยชน์สาธารณะแบบเบ็ดเสร็จแก่เจ้าของข้อมูลส่วนบุคคลเอง การรับข้อมูลส่วนบุคคลจากหน่วยงานของรัฐแห่งอื่นในฐานะที่ กทม. มีหน้าที่ตามพันธกิจในการดำเนินการจัดให้มีศูนย์แลกเปลี่ยนข้อมูลกลางเพื่อสนับสนุนการดำเนินการของหน่วยงานของรัฐในการให้บริการประชาชนผ่านระบบดิจิทัล รวมถึงจากความจำเป็นเพื่อให้บริการตามสัญญาที่อาจมีการแลกเปลี่ยนข้อมูลส่วนบุคคลกับหน่วยงานคู่สัญญาได้</li>
</ol>
<p>นอกจากนี้ ยังหมายความรวมถึงกรณีที่ท่านเป็นผู้ให้ข้อมูลส่วนบุคคลของบุคคลภายนอกแก่ กทม. ดังนี้ ท่านมีหน้าที่รับผิดชอบในการแจ้งรายละเอียดตามนโยบายนี้หรือประกาศของกิจกรรมหรือบริการ ตามแต่กรณี ให้บุคคลดังกล่าวทราบ ตลอดจนขอความยินยอมจากบุคคลนั้นหากเป็นกรณีที่ต้องได้รับความยินยอมในการเปิดเผยข้อมูลแก่ กทม.</p>
<p>ทั้งนี้ ในกรณีที่เจ้าของข้อมูลส่วนบุคคลปฏิเสธม่ให้ข้อมูลที่มีความจำเป็นในการให้บริการของ กทม.อาจเป็นผลให้ กทม. ไม่สามารถให้บริการนั้นแก่เจ้าของข้อมูลส่วนบุคคลดังกล่าวได้ทั้งหมดหรือบางส่วน</p>
