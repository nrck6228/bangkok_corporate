<li>เพื่อการปฏิบัติตามกฎหมาย ประกาศ คำสั่งที่มีผลบังคับใช้ หรือการดำเนินการเกี่ยวกับคดีความ การดำเนินการเกี่ยวกับข้อมูลตามหมายศาล รวมถึงการใช้สิทธิเกี่ยวกับข้อมูลของท่าน</li>
<li><strong>ประเภทบุคคลที่ กทม. เปิดเผยข้อมูลส่วนบุคคลของท่าน</strong></li>
