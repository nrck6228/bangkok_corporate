                            <div class="text-editor-tool">
                                <h3>เชิญผู้เชี่ยวชาญร่วมเสนอแนะมาตรการป้องกันฝุ่นที่เหมาะสมกับบริบทของ กทม.</h3>
                                <p>พล.ต.อ.อัศวิน ขวัญเมือง ผู้ว่าราชการกรุงเทพมหานคร เป็นประธานการประชุมคณะกรรมการป้องกันและแก้ไขปัญหาฝุ่นละอองขนาดเล็ก (PM2.5) ในกรุงเทพมหานคร ครั้งที่ 2/2563 โดยมี 
                                    คณะผู้บริหารกรุงเทพมหานคร หน่วยงานที่เกี่ยวข้องทั้งภายในและภายนอก อาทิ กรมควบคุมมลพิษ กรมควบคุมโรค กรมอนามัย กรมอุตุนิยมวิทยา กรมการขนส่งทางบก.</p>
                                <img src="{{ asset('assets/images/news/photo.jpg')}}" alt="image">
                                <h3>กำหนดแผนปฏิบัติการฝุ่น 3 ระดับ</h3>
                                <p>โฆษกของกรุงเทพมหานคร เปิดเผยว่า ในที่ประชุมวันนี้ผู้เชี่ยวชาญจากสถาบันการศึกษาและหน่วยงานเกี่ยวข้องได้เสนอแนะการป้องกันและแก้ไขปัญหาฝุ่นละออง PM2.5
                                    ในพื้นที่กรุงเทพมหานครหลายประเด็น โดยในส่วนของกรุงเทพมหานครได้เตรียมพร้อมแผนการปฏิบัติการแก้ไขปัญหาฝุ่นละออง PM2.5 ในพื้นที่กรุงเทพมหานคร ปี 2564
                                    โดยเริ่มดำเนินการระหว่างวันที่ 1 ธ.ค. 63 – 28 ก.พ. 64 แบ่งออกเป็นระยะตามสถานการณ์ฝุ่น คือ มาตรการสำหรับสถานการณ์ฝุ่นละอองไม่เกิน 50 มค.ก./ลบ.ม.
                                    จะดำเนินการห้ามรถบรรทุก 6 ล้อขึ้นไปเข้าพื้นที่ชั้นในของกรุงเทพมหานคร ตั้งแต่เวลา 06.00 – 21.00 น. การกำกับดูแลสถานที่ก่อสร้าง บริการตรวจเช็คควันดำเครื่องยนต์ฟรี
                                    ควบคุมโรงงานอุตสาหกรรมในการปล่อยควันดำ การล้างถนน กวดขัน ตรวจตราไม่ให้มีการเผาในที่โล่ง งดกิจกรรมกลางแจ้งของเด็กเล็กและประชาสัมพันธ์ให้สวมใส่หน้ากากอนามัย
                                    ติดตั้งเครื่องวัดปริมาณฝุ่น 50 สำนักงานเขตและสวนสาธารณะ 20 แห่งทั่วกรุง เปิดคลินิกมลพิษทางอากาศในโรงพยาบาลสังกัดกรุงเทพมหานคร ฉีดพ่นละอองน้ำบนอาคารสูง
                                    เปิดสายด่วน 1584 เพื่อแจ้งเบาะแสรถควันดำ และการประชาสัมพันธ์ให้ประชาชนใช้บริการติดตามคุณภาพอากาศผ่านแอปพลิเคชัน มาตรการสำหรับสถานการณ์ที่มีค่าฝุ่นละออง
                                    ขนาดเล็กระหว่าง 50-75 มค.ก./ลบ.ม. จะดำเนินการปิดการเรียนการสอนโรงเรียนสังกัดกรุงเทพมหานคร ครั้งละไม่เกิน 3 วัน การบังคับใช้กฎหมายกับผู้กระทำความผิดในการเผา
                                    การงดกิจกรรมก่อสร้างทุกประเภทที่เกิดฝุ่น การห้ามจอดรถริมถนนในถนนหลัก รอง การจัดให้มีSafe Zone ในทุกโรงเรียนและศูนย์เด็กเล็กสังกัดกรุงเทพมหานคร เก็บขยะมูลฝอย
                                    ให้แล้วเสร็จก่อนเวลา 04.00 น. และการออกหน่วยบริการสาธารณสุขและหน่วยบริการเคลื่อนที่ กทม.</p>
